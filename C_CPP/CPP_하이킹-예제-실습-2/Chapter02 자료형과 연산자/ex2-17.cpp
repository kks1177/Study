// 증감 연산자 - 증감 연산자 사용하기 

#include <iostream>
using namespace std;

int main()
{
	int a = 10, b = 10;

	cout << " Before ===> a : " << a << " b : " << b << endl;
	cout << " a++ ===> a : " << a++ << endl;
	cout << " ++b ===> b : " << ++b << endl;
	cout <<  "After ===> a : " << a << ", b : " << b << endl;
}

