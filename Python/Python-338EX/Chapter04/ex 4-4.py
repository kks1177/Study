for i in range(10):             # 0~9
    print(i, end=" ")
print()  # 줄 바꿈

for i in range(1, 11):          # 1~10
    print(i, end=" ")
print()

for i in range(1, 10, 2):       # 1, 3, 5, 7, 9
    print(i, end=" ")
print()

for i in range(20, 0, -2):      # 20, 18, 16, ..., 2
    print(i, end=" ")

